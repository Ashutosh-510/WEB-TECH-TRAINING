// Using array literal
const fruits = ['apple', 'banana', 'cherry'];
console.log(fruits);


// Creating an instance
const cars = new Array('Honda', 'Toyota', 'Ford');
console.log(cars);



// Using array constructor
const colors = new Array();
colors[0] = 'red';
colors[1] = 'blue';
colors[2] = 'green';
console.log(colors);


